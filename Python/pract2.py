# Define variables with different values
age = 42
salary = 75000.50
name = "Eve"
is_student = False
fruits = ["grape", "kiwi", "mango"]
coordinates = (7, 9)
person = {"name": "Alice", "age": 30}
colors = {"purple", "yellow", "orange"}
empty_variable = "This variable is not empty"

# Print variable values and data types
print("Age:", age)
print("Data Type of Age:", type(age))

print("\nSalary:", salary)
print("Data Type of Salary:", type(salary))

print("\nName:", name)
print("Data Type of Name:", type(name))

print("\nIs Student?", is_student)
print("Data Type of Is Student:", type(is_student))

print("\nFruits:", fruits)
print("Data Type of Fruits:", type(fruits))

print("\nCoordinates:", coordinates)
print("Data Type of Coordinates:", type(coordinates))

print("\nPerson:", person)
print("Data Type of Person:", type(person))

print("\nColors:", colors)
print("Data Type of Colors:", type(colors))

print("\nEmpty Variable:", empty_variable)
print("Data Type of Empty Variable:", type(empty_variable))
